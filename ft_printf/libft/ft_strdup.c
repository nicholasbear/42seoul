#include "libft.h"

char	*ft_strdup(const char *src)
{
	int		length;
	char	*copy;
	int		i;

	length = ft_strlen(src);
	copy = (char *)malloc(sizeof(char) * (length + 1));
	if (!(copy))
		return (NULL);
	i = 0;
	while (src[i])
	{
		copy[i] = src[i];
		i++;
	}
	copy[i] = '\0';
	return (copy);
}
